package com.bigdata.locationweibo.persistent.dbentity;

public class MblogJson {

	private String mblogJsonText;
	private Long createTime;
	private String place_url;
	private String mid;
	
	
	public String getMblogJsonText() {
		return mblogJsonText;
	}
	public void setMblogJsonText(String mblogJsonText) {
		this.mblogJsonText = mblogJsonText;
	}
	public Long getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Long createTime) {
		this.createTime = createTime;
	}
	public String getPlace_url() {
		return place_url;
	}
	public void setPlace_url(String place_url) {
		this.place_url = place_url;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	
	
}
